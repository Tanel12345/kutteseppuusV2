<h3>Kuidas soojuspumba remont tavaliselt algab?</h3>

<p>
Remont algab alati seadme töö kontrollimisest ja rikke põhjuse väljaselgitamisest.
Soojuspumpa ei ole mõistlik hakata parandama enne,
kui on selge, mis tegelikult probleemi põhjustab.
</p>

<p>
Defekteerimise käigus kontrollitakse tavaliselt seadme tööparameetreid,
külmaringi rõhke ja temperatuure,
elektriühendusi ning automaatika tööd.
Vajadusel kontrollitakse ka küttesüsteemi veeringlust,
filtreid ja muid komponente.
</p>
