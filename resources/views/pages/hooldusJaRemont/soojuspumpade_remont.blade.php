<h2><span>Millal remontida</span> – kui soojuspump enam ei tööta nii nagu peaks</h2>

<p>
Soojuspump on tehniliselt üsna keerukas seade ning aastate jooksul võib ette tulla erinevaid rikkeid.
Mõnikord on probleem lihtne – näiteks vigane andur või määrdunud komponent –, teinekord aga vajab seade
põhjalikumat veaotsingut ja remonti. <br>Puhtalt matemaatikale tuginedes on suur osa võimalikest soojuspumba probleemidest, võimalik ja mõistlik, kas hoolduse või remondi käigus kõrvaldada.
</p>

<p>
<strong>Küttesepp OÜ</strong> teostab soojuspumpade remonti enda asukoha tõttu eelkõige <strong>Tallinnas ja Harjumaal</strong>.
Iga remont algab alati <strong>defekteerimisest</strong>,
mille käigus selgitatakse välja rikke tegelik põhjus.
Sageli ei ole probleem mitte ainult seadmes, vaid võib olla seotud ka küttesüsteemi töötingimustega.
</p>
